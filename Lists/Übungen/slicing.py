# Given: numbers = [10, 20, 30, 40, 50, 60, 70, 80, 90]
# Get the first 3 numbers
# Get the last 3 numbers
# Get every other number
# Get the list reversed
# Get items from index 2 to 6

numbers = [10, 20, 30, 40, 50, 60, 70, 80, 90]

print(numbers[0:3]) # the first 3
print(numbers[-3:]) # the last 3
print(numbers[::2]) # every second item#
print(numbers[::-1]) # reversed
print(numbers[1:5]) # from index 2 to 6

