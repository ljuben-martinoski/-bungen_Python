# indexing in a list

fruits = ["apple", "banana", "orange", "grape"]
#          0        1         2         3
#         -4       -3        -2        -1  (negative indexing from end)

print(fruits[0])   # "apple" - first item
print(fruits[2])   # "orange" - third item
print(fruits[-1])  # "grape" - last item
print(fruits[-2])  # "orange" - second from end