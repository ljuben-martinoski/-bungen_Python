"""
Assignment 7 – Even Numbers Only (Easy–Medium)

Task:
Print all even numbers between 1 and 50 using a while loop.

Rules:

Do not use % (modulo)
"""

def even_numbers():
    i = 2 # start from the first even number
    while i  <= 50:
        print(i)
        i += 2 # increment by 2
 
even_numbers()

        

